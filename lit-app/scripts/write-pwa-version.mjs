import { readFileSync, writeFileSync } from "node:fs";

const app = JSON.parse(readFileSync("app.json", "utf8"));
const payload = {
  version: app.expo?.version ?? "1.0.0",
  builtAt: new Date().toISOString(),
};

writeFileSync("public/version.json", `${JSON.stringify(payload, null, 2)}\n`);
console.log(`Wrote public/version.json (${payload.version} @ ${payload.builtAt})`);

// expo-constants' Constants.expoConfig.version is unreliable on web exports (observed
// returning a stale/default "1.0.0" regardless of the real app.json version), so the PWA
// update gate (lib/pwaUpdate.ts) needs the running build's own version baked in as a literal
// source value instead — this file is genuinely bundled at build time, so Metro inlines the
// correct value for THIS build, not whatever expo-constants happens to report at runtime.
const appVersionSource = `// AUTO-GENERATED by scripts/write-pwa-version.mjs — do not edit by hand.
export const APP_VERSION = ${JSON.stringify(payload.version)};
export const APP_BUILT_AT = ${JSON.stringify(payload.builtAt)};
`;
writeFileSync("lib/appVersion.generated.ts", appVersionSource);
console.log("Wrote lib/appVersion.generated.ts");
